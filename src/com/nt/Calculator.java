package com.nt;

public class Calculator {

	//Addition
	
	public Integer add(int a, int b) {
		return a+b;
	}
	
	//Multiplication
	
	public Integer multiply(int a, int b) {
		return a*b;
	}
	
	//Division
	
	public Integer division(int a,int b) {
		return a/b;
	}
	
	//Subtraction
	
	public Integer subtraction(int a,int b) {
		return a-b;
	}
}
